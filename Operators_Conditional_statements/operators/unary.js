//UNARY OPERATOR

//INCREMENT
console.log("a= " , a , "b= " , b);
console.log("++a = " , ++a);
console.log("++b = " , ++b);

a= 2;
b=3;
console.log("a= " , a , "b= " , b);
console.log("a++= " , a++);
console.log("b++ = " , b++);// no change as it prints the value and then increment it which can be reflectedd by printing it again.

//DECREMENT
a=90;
b=45;
console.log("a= " , a , "b= " , b);
console.log("--a = " , --a);
console.log("--b = " , --b);

a=5;
b=7;
console.log("a= " , a , "b= " , b);
console.log("a-- = " , a--);
console.log("b-- = " , b--);// no change as it prints the value and then decrement it which can be reflected by printing it again.
